/* What is variable
Variables in JavaScript are used to store data values.
They act like containers that hold information which can be used later in program.

Java has three ways to declare variables:
1.var
2.let
3.const
*/

//Example 1: using var 
var name = "Aryan";
console.log(name);

//Example 2: using let 
let age = 20;
console.log(age);

//Example 3: using const
const country = "India";
console.log(country);
